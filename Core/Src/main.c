/**
* @brief Function implementing the blink_leds thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_task_blink */
void task_blink(void *argument)
{
  /* USER CODE BEGIN task_blink */
  size_t index = 0;
  HAL_GPIO_WritePin(GPIOB, all_leds, GPIO_PIN_SET);
  /* Infinite loop */
  for(;;)
  {
    switch(mode){
    case 0:
    	HAL_GPIO_WritePin(GPIOB, leds[index], GPIO_PIN_RESET);
    	osDelay(240);
    	HAL_GPIO_WritePin(GPIOB, leds[index], GPIO_PIN_SET);
    	index = (index+1)%4;
    	break;
    case 1:
    	HAL_GPIO_WritePin(GPIOB, leds[index], GPIO_PIN_RESET);
    	osDelay(240);
    	HAL_GPIO_WritePin(GPIOB, leds[index], GPIO_PIN_SET);
    	index = (index-1)%4;
    	break;
    case 2:
    	HAL_GPIO_TogglePin(GPIOB, all_leds);
    	osDelay(240);
    	break;
    case 3:
    	HAL_GPIO_TogglePin(GPIOC, board_led);
    	osDelay(125);
    	break;
    }
  }
  /* USER CODE END task_blink */
}

/* USER CODE BEGIN Header_task_reset */
/**
* @brief Function implementing the reset_control thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_task_reset */
void task_reset(void *argument)
{
  /* USER CODE BEGIN task_reset */
  /* Infinite loop */
  for(;;)
  {
	  if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_3) == GPIO_PIN_SET)
	  	pressed = false;
	  if(pressed){
	    due_time -= 5;
	    if(due_time <= 0){
	      pressed = false;
	      HAL_GPIO_WritePin(GPIOB, all_leds, GPIO_PIN_SET);
	      mode = 3;
	    }
	  }
	  osDelay(5);
  }
  /* USER CODE END task_reset */
}

